package java_code_january_17th_2023;

public class WhileLoop_Concepts {
//if the number of iterations is not pre-defined then we use while loop

	public static void main(String[] args) {
		
		int i = 10;
	
		while(i < 10) { 
			
			System.out.println(i); //10 is printed
			i--;  // i becomes 9
		}

	}

}
